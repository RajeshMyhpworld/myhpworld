// Helper: look up store by slug
export function getStore(slug) {
  return STORES.find((s) => s.slug === slug);
}

// Helper: get all stores in a district
export function getStoresByDistrict(districtSlug) {
  return STORES.filter((s) => s.district === districtSlug);
}

// Helper: find nearest stores to a given PIN code
export function findNearestStores(pin, limit = 5) {
  if (!pin || pin.length !== 6) return STORES.slice(0, limit);
  // Exact match first
  const exact = STORES.filter((s) => s.pinServed?.includes(pin));
  if (exact.length >= limit) return exact.slice(0, limit);
  // Then 3-digit prefix match
  const prefix = pin.slice(0, 3);
  const nearby = STORES.filter((s) => s.pin.startsWith(prefix) && !exact.includes(s));
  return [...exact, ...nearby].slice(0, limit);
}
